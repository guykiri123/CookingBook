// Function to get nutritional values for an ingredient
export const getNutritionForIngredient = (ingredientName, amount, unit) => {
  const nutrition = nutritionDatabase[ingredientName];
  if (!nutrition) {
    console.warn(`Nutrition data not found for: ${ingredientName}`);
    return null;
  }

  // Convert amount to grams
  const conversionFactor = unitConversions[unit] || 1;
  const amountInGrams = amount * conversionFactor;

  // Calculate nutrition values (per 100g values * actual amount / 100)
  return {
    calories: Math.round((nutrition.calories * amountInGrams) / 100),
    protein: Math.round((nutrition.protein * amountInGrams) / 100 * 10) / 10,
    fat: Math.round((nutrition.fat * amountInGrams) / 100 * 10) / 10,
    saturatedFat: Math.round((nutrition.saturatedFat * amountInGrams) / 100 * 10) / 10,
    carbs: Math.round((nutrition.carbs * amountInGrams) / 100 * 10) / 10,
    fiber: Math.round((nutrition.fiber * amountInGrams) / 100 * 10) / 10,
    sugars: Math.round((nutrition.sugars * amountInGrams) / 100 * 10) / 10,
    cholesterol: Math.round((nutrition.cholesterol * amountInGrams) / 100),
    sodium: Math.round((nutrition.sodium * amountInGrams) / 100),
    per100g: nutrition
  };
};

// Function to calculate total nutrition for a recipe
export const calculateRecipeNutrition = (ingredients, servings) => {
  const totals = {
    calories: 0,
    protein: 0,
    fat: 0,
    saturatedFat: 0,
    carbs: 0,
    fiber: 0,
    sugars: 0,
    cholesterol: 0,
    sodium: 0
  };

  ingredients.forEach(ingredient => {
    const nutrition = getNutritionForIngredient(ingredient.name, ingredient.amount, ingredient.unit);
    if (nutrition) {
      totals.calories += nutrition.calories;
      totals.protein += nutrition.protein;
      totals.fat += nutrition.fat;
      totals.saturatedFat += nutrition.saturatedFat;
      totals.carbs += nutrition.carbs;
      totals.fiber += nutrition.fiber;
      totals.sugars += nutrition.sugars;
      totals.cholesterol += nutrition.cholesterol;
      totals.sodium += nutrition.sodium;
    }
  });

  // Per serving values
  const perServing = {
    calories: Math.round(totals.calories / servings),
    protein: Math.round((totals.protein / servings) * 10) / 10,
    fat: Math.round((totals.fat / servings) * 10) / 10,
    saturatedFat: Math.round((totals.saturatedFat / servings) * 10) / 10,
    carbs: Math.round((totals.carbs / servings) * 10) / 10,
    fiber: Math.round((totals.fiber / servings) * 10) / 10,
    sugars: Math.round((totals.sugars / servings) * 10) / 10,
    cholesterol: Math.round(totals.cholesterol / servings),
    sodium: Math.round(totals.sodium / servings)
  };

  return {
    total: totals,
    perServing: perServing
  };
};
